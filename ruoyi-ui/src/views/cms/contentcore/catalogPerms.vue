<template>
  <div class="menu-perms-container" v-loading="loading">
    <el-row :gutter="10" class="mb10">
      <el-col :span="1.5">
        <el-select v-model="currentSiteId" size="mini" @change="handleSiteChange">
          <el-option
            v-for="item in siteOptions"
            :key="item.id"
            :label="item.name"
            :value="item.id"
          />
        </el-select>
      </el-col>
      <el-col :span="1.5">
        <el-button plain type="success" icon="el-icon-edit" size="mini" @click="handleSave">{{ $t("Common.Save") }}</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button plain type="primary" icon="el-icon-check" size="mini" @click="handleSelectAll">{{ this.selectAll ? $t('Common.CheckInverse') : $t('Common.CheckAll') }}</el-button>
      </el-col>
    </el-row>
    <el-row>
      <el-col>
        <el-table
          v-loading="loading"
          :data="catalogPrivs"
          :height="tableHeight"
          :max-height="tableMaxHeight"
          row-key="catalogId"
          default-expand-all
          style="width:100%;line-height: normal;">
          <el-table-column :label="$t('CMS.Catalog.Name')" width="220">
            <template slot-scope="scope">
                <el-checkbox @change="handleRowSelectAll($event, scope.row)" v-model="scope.row.perms['View']">{{ scope.row.name }}</el-checkbox>
            </template>
          </el-table-column>
          <template v-for="(item, index) in catalogPrivItems">
            <el-table-column :key="index" :label="item.name" v-if="item.id!='View'">
              <template slot="header">
                <el-checkbox @change="handleColumnSelectAll(item.id)">{{ item.name }}</el-checkbox>
              </template>
              <template slot-scope="scope">
                <el-checkbox v-if="!(scope.row.children && scope.row.children.length > 0)" v-model="scope.row.perms[item.id]" @change="handleRowColumnChange($event, scope.row)"></el-checkbox>
              </template>
            </el-table-column>
          </template>
        </el-table>
      </el-col>
    </el-row>
  </div>
</template>
<script>
import { getSiteOptions } from "@/api/contentcore/site"
import { getCatalogPermissions, saveCatalogPermissions } from "@/api/contentcore/perms"

export default {
  name: "CatalogPermission",
  props: {
    ownerType: {
      type: String,
      require: false,
      default: ""
    },
    owner: {
      type: String,
      require: false,
      default: ""
    }
  },
  watch: {
    ownerType: {
      handler(newV, oldV) {
        if (newV && newV != '') {
          this.form.ownerType = newV;
          this.loadData();
        }
      }
    },
    owner: {
      handler(newV, oldV) {
        if (newV && newV != '') {
          this.form.owner = newV;
          this.loadData();
        }
      }
    },
  },
  data() {
    return {
      loading: true,
      tableHeight: 600,
      tableMaxHeight: 900,
      selectAll: false,
      selectColumnAll: {},
      catalogPrivs: [],
      catalogPrivItems: [],
      currentSiteId: "",
      siteOptions: []
    };
  },
  created () {
    this.changeTableHeight();
    this.loadData();
    this.loadSiteOptions();
  },
  methods: {
    changeTableHeight () {
      let height = document.body.offsetHeight // 网页可视区域高度
      this.tableHeight = height - 140;
      this.tableMaxHeight = this.tableHeight;
    },
    loadSiteOptions() {
      getSiteOptions().then(response => {
        this.siteOptions = response.data.rows;
      })
    },
    loadData() {
      this.loading = true;
      const params = { ownerType: this.ownerType, owner: this.owner, siteId: this.currentSiteId }
      getCatalogPermissions(params).then(response => {
        this.currentSiteId = response.data.siteId;
        this.catalogPrivs = response.data.catalogPrivs;
        this.catalogPrivItems = response.data.catalogPrivItems;
        this.loading = false;
      });
    },
    handleSiteChange() {
      this.loadData();
    },
    handleSelectAll() {
      this.selectAll = !this.selectAll;
      this.selectCatalogPrivs(this.catalogPrivs, this.selectAll)
    },
    selectCatalogPrivs(arr, checked) {
      arr.forEach(row => {
        Object.keys(row.perms).forEach(key => row.perms[key] = checked)
        if (row.children && row.children.length > 0) {
          this.selectCatalogPrivs(row.children, checked)
        }
      });
    },
    handleRowsSelect(checked, row) {
      let {parentId, catalogId} = row;
      // 子节点被选中，自动处理父节点
      if (parentId !== "0") {
        // 如果是一级栏目，就不用寻找父级点，可直接处理
        // 是否找到路径
        let _findEd = false

        const _setCheck = (item) => {
          const {children} = item
          // 如果找到路径，则进行选择处理
          if (checked) {
            // 如果是选择，则无条件选择即可
            item.perms['View'] = checked
          } else {
            // 如果是取消，要判断所有子项是否都是取消状态
            let childrenChecked = false
            for (let i = 0, l = children.length; i < l; i++) {
              if (children[i].perms['View']) {
                childrenChecked = true
                break
              }
            }
            if (!childrenChecked) {
              // 全部取消，则设定取消，否则选中保持
              item.perms['View'] = false
            }
          }
        }
        const _findPrivs = (arr) => {
          for (let i = 0, l = arr.length; i < l; i++) {
            const {catalogId: id, children} = arr[i]
            if (parentId === id) {
              // 如果当前 item 的 catalogId 就是选中项的 parentId，说明找到了选中项的直接父级，选中项在其 children 中
              // 找到路径
              _findEd = true
            } else if (children && children.length > 0) {
              _findPrivs(children)
            }
            if (_findEd) {
              _setCheck(arr[i])
              break
            }
          }
        }
        _findPrivs(this.catalogPrivs)
      }
    },
    handleRowSelectAll(checked, row) {
      this.$nextTick(() => {
        if (!(row.children && row.children.length > 0)) {
          if (checked) {
            // 如果是选中状态，则选中所有子权限
            this.selectRowAll(this.catalogPrivs, checked, row.catalogId)
          } else {
            // 如果是取消，要判断所有子项是否都是取消状态
            let childrenChecked = false
            Object.keys(row.perms).forEach(key => {
              if (key !== 'View' && row.perms[key]) {
                childrenChecked = true
              }
            })
            if (childrenChecked) {
              // 权限如果还有选择，取消所有选择，仅保留 View 权限
              Object.keys(row.perms).forEach(key => {
                row.perms[key] = false;
              })
              row.perms['View'] = true;
            }
          }
        } else {
          this.selectCatalogPrivs(row.children, checked)
        }
        this.handleRowsSelect(checked, row)
      })
    },
    selectRowAll(arr, value, catalogId) {
      arr.some(row => {
        if (row.catalogId == catalogId) {
          Object.keys(row.perms).forEach(key => {
            row.perms[key] = value;
          })
          return true;
        }
        if (row.children && row.children.length > 0) {
          this.selectRowAll(row.children, value, catalogId)
        }
        return false
      })
    },
    handleColumnSelectAll(column) {
      this.selectColumnAll[column] = !this.selectColumnAll[column];
      this.selectColumn(this.catalogPrivs, column, this.selectColumnAll[column]);
    },
    selectColumn(arr, column, checked) {
      arr.forEach(row => {
        Object.keys(row.perms).forEach(key => {
          if (key == column) {
            row.perms[key] = checked;
            if (checked) {
              row.perms['View'] = checked
            }
          }
        })
        if (row.children && row.children.length > 0) {
          this.selectColumn(row.children, column, checked)
        }
      })
    },
    handleRowColumnChange(checked, row) {
      if (checked) {
        row.perms['View'] = true;
        this.handleRowsSelect(checked, row)
      }
    },
    handleSave() {
      const data = {
        ownerType: this.ownerType,
        owner: this.owner,
        siteId: this.currentSiteId,
        perms: this.catalogPrivs
      };
      saveCatalogPermissions(data).then(response => {
        this.$modal.msgSuccess(this.$t('Common.SaveSuccess'));
      });
    },
    getPermissionKeys(arr, permissions) {
      arr.forEach(row => {
        Object.keys(row.perms).forEach(key => {
          if (row.perms[key]) {
            permissions.push(key + ":" + row.catalogId);
          }
        })
        if (row.children && row.children.length > 0) {
          this.getPermissionKeys(row.children, permissions)
        }
      })
    }
  }
};
</script>